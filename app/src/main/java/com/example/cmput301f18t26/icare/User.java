package com.example.cmput301f18t26.icare;

public class User {

    private String title;
    private int role;


    public User(String name, int role) {
        this.name = title;
        this.role = role;
    }

    public String getName(){
        return this.name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getRole() {
        return this.role;
    }

    public void setRole(int role) {
        this.role = role;
    }
}
